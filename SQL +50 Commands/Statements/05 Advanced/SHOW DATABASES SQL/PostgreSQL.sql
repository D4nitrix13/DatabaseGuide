-- Este comando se utiliza para mostrar una lista de todas las bases de datos disponibles en el servidor PostgreSQL.
\list

-- '\list' es un comando interno de PostgreSQL, utilizado en la interfaz de línea de comandos 'psql'.
-- También puedes usar '\l' como una forma abreviada de este comando.

-- Al ejecutar este comando, obtendrás una lista que incluye todas las bases de datos existentes en el servidor,
-- junto con información adicional como el propietario de cada base de datos, el codificador de caracteres y el estado de conexión.

-- Esto te permite verificar qué bases de datos están disponibles para que puedas conectarte a una específica
-- y realizar operaciones o consultas en ella.
