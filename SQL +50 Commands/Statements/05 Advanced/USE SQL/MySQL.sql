-- Este comando se utiliza para seleccionar una base de datos específica para usar en la sesión actual de MySQL.
USE hello_mysql;

-- 'USE' es la instrucción que permite cambiar el contexto a una base de datos específica.

-- 'hello_mysql' es el nombre de la base de datos a la que deseas acceder. Al ejecutar este comando,
-- todas las operaciones posteriores, como consultas y modificaciones, se realizarán en esta base de datos.

-- Es importante que la base de datos exista; de lo contrario, MySQL mostrará un error indicando que no se encuentra la base de datos.

-- Después de ejecutar este comando, puedes ejecutar comandos SQL sin necesidad de especificar el nombre de la base de datos nuevamente.
