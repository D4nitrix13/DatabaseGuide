-- Este comando se utiliza para mostrar una lista de todas las bases de datos disponibles en el servidor MySQL.
SHOW DATABASES;

-- 'SHOW DATABASES' es una instrucción que permite a los usuarios consultar las bases de datos existentes.

-- Al ejecutar este comando, obtendrás una lista que incluye tanto bases de datos predeterminadas (como 'information_schema', 'mysql' y 'performance_schema') 
-- como cualquier otra base de datos que hayas creado anteriormente.

-- La salida te proporcionará el nombre de cada base de datos y te permitirá verificar cuáles están disponibles 
-- para que puedas seleccionar o trabajar con alguna de ellas posteriormente.
