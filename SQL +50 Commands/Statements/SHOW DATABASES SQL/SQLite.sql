-- Este comando se utiliza para mostrar una lista de todas las bases de datos adjuntas a la conexión actual en SQLite.
.databases 
-- '.databases' es un comando interno de la interfaz de línea de comandos de SQLite que muestra información sobre las bases de datos conectadas.

-- Al ejecutar este comando, obtendrás una lista que incluye cada base de datos adjunta,
-- junto con su nombre y la ruta al fichero correspondiente en el sistema de ficheros.

-- Esto te permite verificar qué bases de datos están disponibles en la sesión actual de SQLite,
-- facilitando la gestión y el acceso a múltiples bases de datos si es necesario.