-- Este comando crea una nueva base de datos llamada 'hello_postgresql' en PostgreSQL.
CREATE DATABASE hello_postgresql;

-- 'CREATE DATABASE' es la sintaxis utilizada en PostgreSQL para crear una nueva base de datos.

-- 'hello_postgresql' es el nombre que le das a la base de datos que estás creando. 
-- Puedes cambiar este nombre según tu preferencia, siempre que sigas las reglas de nomenclatura de PostgreSQL 
-- (por ejemplo, no usar palabras reservadas o caracteres especiales).

-- Después de ejecutar este comando, la base de datos 'hello_postgresql' estará disponible en tu servidor PostgreSQL 
-- y podrás empezar a usarla para crear tablas, insertar datos y realizar consultas.
