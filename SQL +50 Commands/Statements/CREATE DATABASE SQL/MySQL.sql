-- Este comando crea una nueva base de datos llamada 'hello_mysql' en MySQL.
CREATE DATABASE hello_mysql;

-- 'CREATE DATABASE' es la sintaxis básica que MySQL usa para crear una nueva base de datos.

-- 'hello_mysql' es el nombre de la base de datos que estás creando. Puedes cambiar este nombre a cualquier otro que prefieras, 
-- siempre y cuando siga las reglas de nombres en MySQL (por ejemplo, no puede empezar con un número ni contener caracteres especiales).

-- Una vez que se ejecute este comando, la base de datos 'hello_mysql' estará disponible en tu servidor MySQL 
-- y podrás usarla para crear tablas, almacenar datos, entre otras operaciones.
